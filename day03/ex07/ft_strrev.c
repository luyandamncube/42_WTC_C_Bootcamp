char	*ft_strrev(char *str)
{
	int		k;
	int		m;
	char	c;

	k = 0;
	m = 0;
	while (str[k] != '\0')
	{
		k++;
	}
	k--;
	while (m < k)
	{
		c = str[m];
		str[m] = str[k];
		str[k] = c;
		m++;
		k--;
	}
	return (str);
}
